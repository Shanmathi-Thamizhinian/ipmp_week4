class Solution {
  public:
    vector<int> firstAndLast(vector<int> &arr, int n, int x) {
        int fo=-1,lo=-1;
        vector<int> ans;
        int low=0,high=n-1;
        while(low<=high){
            int mid=(low+high)/2;
            if(arr[mid]==x){
                lo=mid;
                low=mid+1;
            }
            else if(arr[mid]<x){
                low=mid+1;
            }
            else{
                high=mid-1;
            }
        }
        low=0,high=n-1;
        while(low<=high){
            int mid=(low+high)/2;
            if(arr[mid]==x){
                fo=mid;
                high=mid-1;
            }
            else if(arr[mid]<x){
                low=mid+1;
            }
            else{
                high=mid-1;
            }
        }
        if(fo==-1 && lo==-1){
            return {-1};
        }
        ans.push_back(fo);
        ans.push_back(lo);
        return ans;
    }
};

